package model;

/**
 *
 * @author aluno
 */
public class Atleta {
    private String nome;
    private float peso;
    private float altura;
    private int idade;
    
    public Atleta(String nome, float peso, float altura, int idade){
        this.nome = nome;
        this.peso = peso;
        this.altura = altura;
        this.idade = idade;
    }
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
